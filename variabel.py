# Materi: Variabel di Python
# Contoh penggunaan variabel dengan nama "Dara"

# Membuat variabel
nama = "Dara"           # Variabel string untuk menyimpan nama
umur = 15               # Variabel integer untuk menyimpan umur
asal_kota = "karawang"   # Variabel string untuk menyimpan kota asal

# Menampilkan isi variabel
print("=== Data Siswa ===")
print("Nama       :", nama)
print("Umur       :", umur, "tahun")
print("Asal Kota  :", asal_kota)

# Variabel bisa diubah nilainya
nama = "Dara permatasari"
umur = 16

# Menampilkan data setelah diubah
print("\n=== Data Setelah Update ===")
print("Nama       :", nama)
print("Umur       :", umur, "tahun")
